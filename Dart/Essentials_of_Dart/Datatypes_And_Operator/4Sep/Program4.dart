void main(){
	print("Pre inc and Post inc Code");
	
	int x = 10;

	print(++x); // 11
	print(x++); // 11
	print(x);  // 12
	
	int y = 5;
	
	print(++y); // 6
	print(y++); // 6 
	print(y); // 7

	int z = -5;

	print(++z); // -4
	print(z++); // -4
	print(z); // -3
	
}
