// switch case with do while
void main(){

	print("switch case code with do while");
	int x = 3;
	
	do{
		print("C");
		print("CPP");
		print("JAVA");
		print("Dart");
	switch(x){
		case 1:
			print("1972");
		case 2:
			print("1978");
		case 3:
			print("1995");
		case 4:
			print("2013");
		default:
			print("No-Match");
	}
	
	}while(false);
}
