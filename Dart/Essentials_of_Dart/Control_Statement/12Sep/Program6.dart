// switch case 

void main(){

	print("switch case code");
	int x = 5;
	//num x = 1;
	//var x = 1.0;
	//dynamic x = "monday";
	//String x = "1.0";
	switch(x){
		case 1.0:
			print("One");
		case 2.0:
			print("Two");
		case 3.0:
			print("Three");
		case 4.0:
			print("Four");
		case "monday":
			print("Monday");
		default:
			print("No-Match");
	}
}
